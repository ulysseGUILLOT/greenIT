            <div id="panelsStayOpen-collapseTwo" class="accordion-collapse collapse"
                 aria-labelledby="panelsStayOpen-headingTwo">
                <div class="accordion-body">
                    <p>En tant que développeur en cybersécurité dans le domaine de l'aéronautique, j'essaye de prendre
                        en
                        compte plusieurs notions de la Green IT vues en cours dans mon travail :</p>

                    <p>Lors de la conception des algorithmes de sécurité, j'essaye de prendre en compte la charge de
                        traitement sur les sytèmes pour la minimiser au maximum. Je privilégie des langages et des
                        outils
                        légers, souvent de bas niveau, pour garantir une faible consommation et une bonne
                        certificabilité
                        (notion très importante dans l'aéronautique).</p>

                    <p>Afin de tester mes développements, je m'efforce autant que possible d'effectuer des tests dans
                        des
                        environnements virtuels plutôt que d'investir dans des équipements de test physiques
                        spécifiques.
                        Cette approche non seulement contribue à réduire les coûts, mais aussi à minimiser l'empreinte
                        environnementale de mes activités professionnelles.</p>

                    <p>L'application des principes de la Green IT dans le domaine de l'aéronautique s'avère complexe en
                        raison de la nature intrinsèquement énergivore de ce domaine. Les avions, par leur conception
                        même,
                        génèrent des émissions significatives. De plus, la certificabilité rigoureuse des systèmes
                        aéronautiques impose des contraintes strictes sur les modifications potentielles, limitant la
                        flexibilité pour intégrer des technologies plus respectueuses de l'environnement. La redondance,
                        impérative pour assurer la sécurité et la fiabilité des systèmes embarqués, peut également
                        entraîner
                        une surconsommation de ressources.</p>

                    <p>Selon moi, la plus grande marge de progression écologique dans le domaine de l'aéronautique
                        réside
                        principalement dans la transformation de la nature de la propulsion, plutôt que dans des
                        ajustements
                        au niveau des systèmes informatiques embarqués.</p>

                    <p>En dehors de mon alternance, je m'efforce d'appliquer les principes de la Green IT à mes projets
                        personnels.</p>

                    <p>J'ai adopté l'habitude d'utiliser des extensions de navigateur telles que GreenIT-analysis et
                        Lighthouse pour analyser mon code et l'améliorer en fonction des résultats obtenus.</p>

                    <p>Pour l'hébergement de mes sites internet, j'ai choisi de les héberger sur ma Freebox. Cette
                        approche
                        présente un intérêt économique et écologique, car la Freebox est constamment allumée. Ainsi, son
                        utilisation s'avère plus économe que le déploiement d'un serveur spécifique uniquement pour
                        cette
                        fin.</p>

                    <p>Par ailleurs, je m'efforce de minimiser mon code en utilisant des frameworks légers, en
                        comprimant
                        les fichiers et en limitant l'utilisation de médias. Cette approche contribue non seulement à
                        améliorer les performances de mes projets, mais également à réduire leur impact environnemental
                        en
                        optimisant l'utilisation des ressources informatiques.</p>
