                        produits.</p>

                    <p>Par ailleurs, Safran a mis en place une politique de télétravail permettant aux employés de
                        réduire
                        leurs déplacements et, par conséquent, de diminuer leur empreinte carbone liée aux trajets.</p>

                    <p>Malgré les importantes contraintes liées à la sécurité des données, je pense que Safran pourrait
                        améliorer son utilisation du numérique en optimisant ses serveurs internes, en formant et
